#include "lib_ft_printf.h"

void	check_flags(const char *s, int l, int x)
{
	if (s[l] == 'd')
		ft_putnbr_fd(x, 1);
	else if (s[l] == 'c')
		ft_putchar_fd(x, 1);	
}
